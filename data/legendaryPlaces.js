export const legendaryPlacesById = new Map(
  legendaryPlaces.flatMap((place) => [
    [place.id, place],
    ...(place.legacyIds || []).map((legacyId) => [legacyId, place]),
  ])
);


export function canonicalLegendaryPlaceId(placeId) {
  return legendaryPlacesById.get(placeId)?.id || placeId;
}

export function hydrateLegendaryPlace(place = {}) {
  const local = legendaryPlacesById.get(place.id);
  if (!local) return place;
  return {
    ...local,
    ...place,
    id: local.id,
    localImage: local.localImage,
    defaultImagePath: local.imagePath,
    story: place.story || place.description || local.story,
    description: place.description || place.story || local.story,
    icon: place.icon || local.icon,
    discoveryRadiusMeters: Number(place.discoveryRadiusMeters || local.discoveryRadiusMeters || 220),
  };
}
